using Moo.Domain.Auth;
using Moo.Domain.DataInterfaces;
using Moo.Entities.Interfaces;
using System;
using System.Web.Mvc;
using System.Web.Security;

namespace Moo.Domain.IdentityProviders
{
    public class AuthenitcationProvider : MembershipProvider, IAuthenticationProvider
    {
        public IUnitOfWork Unit
        {
            get { return DependencyResolver.Current.GetService<IUnitOfWork>(); }
        }

        public override bool ValidateUser(string username, string password)
        {
            if (string.IsNullOrEmpty(username) || string.IsNullOrEmpty(password))
            {
                return false;
            }

            var user = Unit.Users.Get(username, password);
            return (user != null) ? true : false;
        }

        public override MembershipUser GetUser(string username, bool userIsOnline)
        {
            var user = Unit.Users
                .Include("Roles")
                .Get(username);

            if (user == null)
            {
                return null;
            }
            return new CustomMembershipUser(user);
        }

        public override string GetUserNameByEmail(string email)
        {
            var user = Unit.Users.GetByEmail(email);
            if (user != null)
                return user.Username;

            return string.Empty;
        }

